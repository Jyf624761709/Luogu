/* 月用电量在150千瓦时及以下部分按每千瓦时0.4463元执行，
月用电量在151~400千瓦时的部分按每千瓦时0.4663元执行，
月用电量在401千瓦时及以上部分按每千瓦时0.5663元*/
#include<cstdio>
int main(){
	int d;
	double s=0;
	scanf("%d",&d);
	if(d<=150)
	    s=d*0.4463;
    if(d>150&&d<401)
    	s=150*0.4463+(d-150)*0.4663;
    if(d>400)
	    s=150*0.4463+(400-150)*0.4663+(d-400)*0.5663;
	printf("%.1lf",s);
	return 0;
}
